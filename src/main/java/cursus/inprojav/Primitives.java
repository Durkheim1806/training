package cursus.inprojav;

public class Primitives {

    public static void main(String[] args) {

        byte temperature = 200;
        short maxtemperature = 45;
        int mintemperature = -1_000;
        long windpower = 34L;
        float drijf = 1.0F;
        double macht = 1.0E2;


        System.out.println("temp: " + temperature);
        System.out.println("max temp: " + maxtemperature);
        System.out.println("min temp: " + mintemperature);
        System.out.println("windpower: " + windpower);
        System.out.println("macht: " + macht);
        System.out.println("drijf: " + drijf);
    }
}
